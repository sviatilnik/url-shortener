package generators

// Generator определяет интерфейс для генерации коротких кодов.
// Различные реализации могут использовать разные алгоритмы генерации.
type Generator interface {
	// Get генерирует короткий код для переданной строки.
	// Возвращает сгенерированный код или ошибку.
	Get(str string) (string, error)
}
